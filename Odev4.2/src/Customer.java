
public class Customer {
	
		int Id;
		String FirstName;
		String LastName;
		String DateOfBirth;
		String NationalityId;
		
		public Customer() {
			
		}
		public Customer(int id, String firstName, String lastName, String dateOfBirth, String nationalityId) {
			super();
			Id = id;
			FirstName = firstName;
			LastName = lastName;
			DateOfBirth = dateOfBirth;
			NationalityId = nationalityId;
		}		
}
